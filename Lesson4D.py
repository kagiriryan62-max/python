# Python While Loop.
# While loop executes a block of code repeatedly as long as a certain conition is true. The syntax for the while loop in python is as follows:
"""
Initialization of a variable,
While keyword,
Followed by the condition/statement to be evaluated,
Followed by a full colon,
Followed by the code to be printed out,
The increment/decrement
"""

number = 0
while number < 5:
    print("Hello Moses", number)
    number = number + 1

print("==================================")
#Create a python program that prints the even numbers from 50 to 70 using the while loop.
number = 50
while number < 70:
    print(number)
    number = number + 2

print("====================================")
# Below is a decrement example.
number = 201
while number >= 150:
    print(number)
    number = number - 3

